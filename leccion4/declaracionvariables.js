//uso de let para declaracion de variables
/**let te permite declarar variables limitando su alcance (scope) al bloque, 
 * declaración, o expresión donde se está usando. a diferencia 
 * de la palabra clave var la cual define una variable global o 
 * local en una función sin importar el ámbito del bloque. */

let nombre = "Juan";

let nombreCompleto = "Juan Perez";

console.log(nombre);
console.log(nombreCompleto);